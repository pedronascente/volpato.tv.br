                </li>
                <li>
                    <b>Operador:</b> quem realiza tratamento de dados em nome do Controlador;
                </li>
                <li>    
                    <b>Endereço  IP  (Internet  Protocol):</b> é  um  número  exclusivo  que  identifica  um computador em uma rede.
